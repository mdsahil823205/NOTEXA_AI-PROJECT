const PDFDocument = require("pdfkit");

const PDFController = async (req, res) => {
  try {
    const { result } = req.body;

    if (!result || !result.content) {
      return res.status(404).json({ message: "No content available" });
    }

    const content = result.content;

    const doc = new PDFDocument({
      margins: 60,
      size: "A4",
      bufferPages: true,
    });

    res.setHeader("Content-Type", "application/pdf");
    res.setHeader(
      "Content-Disposition",
      "attachment; filename=NotexaAI_Notes.pdf"
    );

    doc.pipe(res);

    // ===== COVER HEADER =====
    doc
      .fontSize(26)
      .font("Helvetica-Bold")
      .fillColor("#1e3a8a")
      .text("NotexaAI", { align: "center" });

    doc
      .fontSize(12)
      .fillColor("#6b7280")
      .text("Smart Study Companion", { align: "center" });

    doc.moveDown(2);

    // ===== TITLE =====
    doc
      .fontSize(20)
      .font("Helvetica-Bold")
      .fillColor("#111827")
      .text(result.topic || "Study Notes");

    doc.moveDown(1);

    doc
      .fontSize(12)
      .font("Helvetica")
      .fillColor("#374151")
      .text(`Overall Importance: ${content.importance || "High"}`);

    doc.moveDown(2);

    // ===== SUBTOPICS =====
    doc
      .fontSize(16)
      .font("Helvetica-Bold")
      .fillColor("#1e40af")
      .text("Sub Topics");

    doc.moveDown(1);

    Object.entries(content.subTopics || {}).forEach(([heading, topics]) => {
      doc
        .fontSize(13)
        .font("Helvetica-Bold")
        .fillColor("#111827")
        .text(heading.replace(/[⭐]/g, "*"));

      doc.moveDown(0.5);

      topics.forEach((t) => {
        doc
          .fontSize(11)
          .font("Helvetica")
          .fillColor("#374151")
          .text(`• ${t}`, { indent: 20 });
      });

      doc.moveDown(1);
    });

    // ===== DETAILED NOTES =====
    doc.addPage();

    doc
      .fontSize(16)
      .font("Helvetica-Bold")
      .fillColor("#1e40af")
      .text("Detailed Notes");

    doc.moveDown(1);

    const cleanNotes = content.notes
      .replace(/#/g, "")
      .replace(/\*/g, "")
      .replace(/`/g, "");

    doc
      .fontSize(11)
      .font("Helvetica")
      .fillColor("#111827")
      .text(cleanNotes, {
        align: "justify",
        lineGap: 4,
      });

    // ===== QUESTIONS =====
    doc.addPage();

    doc
      .fontSize(16)
      .font("Helvetica-Bold")
      .fillColor("#1e40af")
      .text("Practice Questions");

    doc.moveDown(1);

    if (content.questions?.short) {
      doc.fontSize(13).font("Helvetica-Bold").text("Short Questions");
      doc.moveDown(0.5);

      content.questions.short.forEach((q, i) => {
        doc
          .fontSize(11)
          .font("Helvetica")
          .text(`${i + 1}. ${q}`, { indent: 20 });
      });
    }

    doc.moveDown(1);

    if (content.questions?.long) {
      doc.fontSize(13).font("Helvetica-Bold").text("Long Questions");
      doc.moveDown(0.5);

      content.questions.long.forEach((q, i) => {
        doc
          .fontSize(11)
          .font("Helvetica")
          .text(`${i + 1}. ${q}`, { indent: 20 });
      });
    }

    // ===== FOOTER ON EVERY PAGE =====
    const pages = doc.bufferedPageRange();

    for (let i = 0; i < pages.count; i++) {
      doc.switchToPage(i);
      doc
        .fontSize(9)
        .fillColor("#9ca3af")
        .text(
          `Generated by NotexaAI | Page ${i + 1} of ${pages.count}`,
          0,
          doc.page.height - 40,
          { align: "center" }
        );
    }

    doc.end();
  } catch (err) {
    console.error("PDF Error:", err);
    if (!res.headersSent) {
      res.status(500).json({ message: "Internal server error" });
    }
  }
};

module.exports = PDFController;